// on inclut les définitions nécessaires
#include "stdafx.h"
#include "JFCException.h"

//////////////////////////////////////////////////////////////////////////////
// les constructeurs
//////////////////////////////////////////////////////////////////////////////

Exception::Exception()
{
	// on initialise le code de l'exception
	m_Code = 0;
}

Exception::Exception(long code)
{
	// on initialise le code de l'exception
	m_Code = ((code < 0) || (code > 65535)) ? 0 : code;
}

Exception::Exception(const Exception & source)
{
	// on recopie le code de l'exception
	m_Code = source.m_Code;
}

//////////////////////////////////////////////////////////////////////////////
// l'opérateur d'affectation
//////////////////////////////////////////////////////////////////////////////

void Exception::operator =(const Exception & source)
{
	// on recopie le code de l'exception
	m_Code = source.m_Code;
}

//////////////////////////////////////////////////////////////////////////////
// la fonction pour réinitialiser l'exception
//////////////////////////////////////////////////////////////////////////////

void Exception::Reset()
{
	// on réinitialise le code de l'exception
	m_Code = 0;
}

//////////////////////////////////////////////////////////////////////////////
// le destructeur
//////////////////////////////////////////////////////////////////////////////

Exception::~Exception()
{
	// on ne fait rien
}
